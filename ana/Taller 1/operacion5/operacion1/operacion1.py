# Operaciones matemáticas básicas

# a. (5+5)*5
resultado_a = (5 + 5) * 5
print("a. (5+5)*5 =", resultado_a)

# b. 5+(5*5)
resultado_b = 5 + (5 * 5)
print("b. 5+(5*5) =", resultado_b)

# c. 5 Modulo 2
resultado_c = 5 % 2
print("c. 5 Modulo 2 =", resultado_c)

# d. (5-(6/3))*2
resultado_d = (5 - (6 / 3)) * 2
print("d. (5-(6/3))*2 =", resultado_d)