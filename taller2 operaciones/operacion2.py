# Valores iniciales
a = 7
b = 3

# a. a != b
resultado_a = a != b
print("a. a != b:", resultado_a)

# b. a == b
resultado_b = a == b
print("b. a == b:", resultado_b)

# c. a > b
resultado_c = a > b
print("c. a > b:", resultado_c)

# d. (a + b) < 2
resultado_d = (a + b) < 2
print("d. (a + b) < 2:", resultado_d)

# e. (a > 7) Y (b = 3)
resultado_e = (a > 7) and (b == 3)
print("e. (a > 7) Y (b = 3):", resultado_e)

# f. (a < 7) O (b == 3)
resultado_f = (a < 7) or (b == 3)
print("f. (a < 7) O (b == 3):", resultado_f)

# g. (a < 7) O (b != 3)
resultado_g = (a < 7) or (b != 3)
print("g. (a < 7) O (b != 3):", resultado_g)
